package prostmk.Model.Enumeration;

public class EnumSample {

    public static void res(Cities cities){
        switch (cities){
            case MINSK:
                System.out.println("Minsk");break;
            case MOSCOW:
                System.out.println("Moscow");break;
            case SANFRANCISCO:
                System.out.println("San Francisco");break;
        }
    }

}
